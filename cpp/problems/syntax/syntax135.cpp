#include <iostream>
using namespace std;

int main(int argc, char * argv[]) {
    cout << "sizeof(char) = " << sizeof(char) << "\n";
    cout << "sizeof(int) = " << sizeof(int) << "\n";
    cout << "sizeof(double) = " << sizeof(double) << "\n";
    cout << "sizeof(float) = " << sizeof(float) << "\n";
    return 0;
}
